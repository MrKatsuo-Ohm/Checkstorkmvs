const StockModel = {
  getAll() { return stockItems; },
  getById(id) { return stockItems.find(i => i.id === id); },
  create(data) {
    const newItem = { id: uuidv4(), ...data, created_at: new Date().toISOString(), updated_at: new Date().toISOString() };
    stockItems.push(newItem);
    return newItem;
  },
  update(id, data) {
    const idx = stockItems.findIndex(i => i.id === id);
    if (idx === -1) return null;
    stockItems[idx] = { ...stockItems[idx], ...data, id, updated_at: new Date().toISOString() };
    return stockItems[idx];
  },
  delete(id) {
    const idx = stockItems.findIndex(i => i.id === id);
    if (idx === -1) return false;
    stockItems.splice(idx, 1);
    return true;
  },
  getStats() {
    const total = stockItems.length;
    const totalQty = stockItems.reduce((s, i) => s + i.quantity, 0);
    const lowStock = stockItems.filter(i => i.min_stock > 0 && i.quantity <= i.min_stock).length;
    const totalValue = stockItems.reduce((s, i) => s + (i.quantity * i.price), 0);
    return { total, totalQty, lowStock, totalValue };
  }
};

